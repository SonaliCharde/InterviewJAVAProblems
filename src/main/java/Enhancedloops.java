import java.util.Arrays;

public class Enhancedloops {
    public static void main (String[]args){
        int primes[]={2,3,5,7,9,11,13,17,19,23, 29};
        for (int t:primes){
            System.out.println(t);
        }
        String lang[]={"a","b","c","d"};
        for (String st:lang) {
            System.out.println(st);
        }
    }
}
